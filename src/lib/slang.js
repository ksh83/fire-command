// 소방 은어 / STT 오인식 보정 사전
// 현장 무전에서 자주 쓰이는 표현을 표준 용어로 교정

const SLANG_MAP = [
  // ── 출동 호칭 ──────────────────────────────────────────────
  [/덕진\s*일\b/g, '덕진1'],
  [/덕진\s*이\b/g, '덕진2'],
  [/덕진\s*삼\b/g, '덕진3'],
  [/금암\s*일\b/g, '금암1'],
  [/금암\s*이\b/g, '금암2'],

  // ── 차량 은어 ──────────────────────────────────────────────
  [/팬\s*탱\b/g, '펌탱크차'],
  [/펌\s*탱\b/g, '펌탱크차'],
  [/탱크\s*차\b/g, '물탱크차'],
  [/고가\s*차\b/g, '고가사다리차'],
  [/굴절\s*차\b/g, '굴절사다리차'],
  [/화학\s*차\b/g, '화학소방차'],
  [/배연\s*차\b/g, '배연차량'],
  [/구조\s*공작차/g, '구조공작차'],

  // ── 소방 작전 용어 ────────────────────────────────────────
  [/주\s*수\b/g, '주수'],
  [/방\s*수\b/g, '방수'],
  [/진\s*화\b/g, '진화'],
  [/압\s*제\b/g, '화선압제'],
  [/잔\s*화\b/g, '잔화'],
  [/완\s*진\b/g, '완진'],
  [/배\s*연\b/g, '배연환기'],
  [/환\s*기\b/g, '배연환기'],
  [/진\s*입\b/g, '진입'],
  [/수\s*원\b/g, '수원확보'],
  [/소화\s*전/g, '소화전'],

  // ── 인명 용어 ─────────────────────────────────────────────
  [/요\s*구\s*자\b/g, '요구조자'],
  [/요\s*구\s*조\b/g, '요구조자'],
  [/구\s*조\s*완\s*료/g, '구조완료'],
  [/사\s*상\s*자/g, '사상자'],
  [/경\s*상\b/g, '경상'],
  [/중\s*상\b/g, '중상'],

  // ── 대응 단계 ─────────────────────────────────────────────
  [/일\s*단\s*계/g, '대응1단계'],
  [/이\s*단\s*계/g, '대응2단계'],
  [/삼\s*단\s*계/g, '대응3단계'],
  [/대응\s*1/g, '대응1단계'],
  [/대응\s*2/g, '대응2단계'],
  [/대응\s*3/g, '대응3단계'],

  // ── 건물 구조 ─────────────────────────────────────────────
  [/필로\s*디\b/g, '필로티'],
  [/피\s*엘\b/g, '필로티'],
  [/스\s*프\s*링/g, '스프링클러'],
  [/준\s*공\s*연\s*도/g, '준공연도'],

  // ── 지휘 용어 ─────────────────────────────────────────────
  [/지\s*령\b/g, '지령'],
  [/선\s*착\s*대\b/g, '선착대'],
  [/지\s*휘\s*소\b/g, '지휘소'],
  [/지\s*휘\s*권\b/g, '지휘권'],
  [/대\s*응\s*단\s*장/g, '대응단장'],
  [/지\s*휘\s*팀\s*장/g, '지휘팀장'],
  [/선\s*착\s*대\s*장/g, '선착대장'],

  // ── 기상 / 야간 ───────────────────────────────────────────
  [/야\s*간\b/g, '야간'],
  [/주\s*간\b/g, '주간'],
]

/**
 * STT 텍스트에 소방 은어 보정을 적용한다.
 * @param {string} text - 원본 STT 텍스트
 * @returns {{ corrected: string, changes: string[] }} 보정 결과
 */
export function correctSlang(text) {
  let corrected = text
  const changes = []

  for (const [pattern, replacement] of SLANG_MAP) {
    const before = corrected
    corrected = corrected.replace(pattern, replacement)
    if (corrected !== before) {
      // 무엇이 바뀌었는지 기록 (중복 제거)
      const match = before.match(pattern)
      if (match) {
        const entry = `${match[0]} → ${replacement}`
        if (!changes.includes(entry)) changes.push(entry)
      }
    }
  }

  return { corrected, changes }
}
