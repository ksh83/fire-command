export async function extractKeywords(text) {
  try {
    const raw = await callClaude({
      system: SYSTEM,
      user: `무전 텍스트:\n${text}`,
      maxTokens: 500,
    })

    // JSON 파싱
    const jsonStr = raw.match(/\{[\s\S]*\}/)?.[0]
    if (!jsonStr) return null
    return JSON.parse(jsonStr)
  } catch (e) {
    console.error('[AgentA] 오류:', e)
    return null
  }
}
